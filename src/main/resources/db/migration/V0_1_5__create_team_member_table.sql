CREATE TABLE IF NOT EXISTS team_members
(
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    employee_id BIGINT,
    team_id     BIGINT,
    CONSTRAINT pk_team_members PRIMARY KEY (id),
    CONSTRAINT FK_TEAM_MEMBERS_ON_EMPLOYEE FOREIGN KEY (employee_id) REFERENCES employees (id),
    CONSTRAINT FK_TEAM_MEMBERS_ON_TEAM FOREIGN KEY (team_id) REFERENCES teams (id)
);
